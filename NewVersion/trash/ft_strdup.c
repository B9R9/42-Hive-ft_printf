#include "libft.h"
#include <stdio.h>

char	*ft_strdup(const char *s1)
{
	char	*dup;
	int		i;
	
	i = 0;
	dup = ft_strnew(ft_strlen(s1));
	if (!dup)
		return (NULL);
	while (s1[i] != '\0')
	{
		dup[i] = s1[i];
		i++;
	}
	return (dup);
}
